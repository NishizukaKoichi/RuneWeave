fn generate_tool(
    output_dir: &Path,
    tool: &crate::types::ToolConfig,
    plan: &Plan,
    tera: &mut Tera,
    context: &mut Context,
    files: &mut Vec<String>,
) -> Result<()> {
    let tool_dir = output_dir.join("tools").join(&tool.name);
    fs::create_dir_all(&tool_dir)?;

    let cargo_content = format!(
        r#"[package]
name = "{}"
version = "{}"
edition = "2021"
rust-version = "{}"

[[bin]]
name = "{}"
path = "src/main.rs"

[dependencies]
clap = {{ version = "4.5", features = ["derive"] }}
tokio = {{ workspace = true }}
serde = {{ workspace = true }}
serde_json = {{ workspace = true }}
anyhow = {{ workspace = true }}
tracing = {{ workspace = true }}
tracing-subscriber = {{ workspace = true }}
"#,
        tool.name,
        plan.project.version,
        plan.project.rust_version.as_deref().unwrap_or("1.80"),
        tool.name,
    );

    fs::write(tool_dir.join("Cargo.toml"), cargo_content)?;
    files.push(format!("tools/{}/Cargo.toml", tool.name));

    let main_content = format!(
        r#"//! {}

use clap::Parser;
use anyhow::Result;

#[derive(Parser)]
#[command(name = "{}", about = "{}", version)]
struct Cli {{
    #[arg(short, long, help = "Enable verbose output")]
    verbose: bool,
}}

#[tokio::main]
async fn main() -> Result<()> {{
    let cli = Cli::parse();

    if cli.verbose {{
        tracing_subscriber::fmt()
            .with_max_level(tracing::Level::DEBUG)
            .init();
    }} else {{
        tracing_subscriber::fmt()
            .with_max_level(tracing::Level::INFO)
            .init();
    }}

    tracing::info!("Running {}", env!("CARGO_PKG_NAME"));
    
    Ok(())
}}
"#,
        tool.description,
        tool.name,
        tool.description,
    );

    let src_dir = tool_dir.join("src");
    fs::create_dir_all(&src_dir)?;
    fs::write(src_dir.join("main.rs"), main_content)?;
    files.push(format!("tools/{}/src/main.rs", tool.name));

    Ok(())
}

fn write_github_workflows(
    output_dir: &Path,
    plan: &Plan,
    tera: &mut Tera,
    context: &mut Context,
    files: &mut Vec<String>,
) -> Result<()> {
    let workflow_dir = output_dir.join(".github/workflows");
    fs::create_dir_all(&workflow_dir)?;

    let ci_content = format!(
        r#"name: CI

on:
  push:
    branches: [ main, develop ]
  pull_request:
    branches: [ main, develop ]

env:
  CARGO_TERM_COLOR: always
  RUST_VERSION: {}

jobs:
  test:
    name: Test
    runs-on: ubuntu-latest
    steps:
    - uses: actions/checkout@v4
    
    - name: Install Rust
      uses: dtolnay/rust-toolchain@stable
      with:
        toolchain: ${{{{ env.RUST_VERSION }}}}
        targets: x86_64-unknown-linux-musl,wasm32-unknown-unknown
        components: rustfmt,clippy
    
    - name: Cache cargo
      uses: actions/cache@v3
      with:
        path: |
          ~/.cargo/bin/
          ~/.cargo/registry/index/
          ~/.cargo/registry/cache/
          ~/.cargo/git/db/
          target/
        key: ${{{{ runner.os }}}}-cargo-${{{{ hashFiles('**/Cargo.lock') }}}}
    
    - name: Check formatting
      run: cargo fmt --all -- --check
    
    - name: Clippy
      run: cargo clippy --workspace --all-targets --all-features -- -D warnings
    
    - name: Test
      run: cargo test --workspace --all-features
    
    - name: Build x86_64-musl
      run: cargo build --release --target x86_64-unknown-linux-musl
    
    - name: Build wasm32
      run: cargo build --release --target wasm32-unknown-unknown

  sbom:
    name: Generate SBOM
    runs-on: ubuntu-latest
    needs: test
    steps:
    - uses: actions/checkout@v4
    
    - name: Install Syft
      uses: anchore/sbom-action/download-syft@v0
    
    - name: Generate SBOM
      run: syft dir:. -o spdx-json > sbom.spdx.json
    
    - name: Upload SBOM
      uses: actions/upload-artifact@v3
      with:
        name: sbom
        path: sbom.spdx.json

  sign:
    name: Sign artifacts
    runs-on: ubuntu-latest
    needs: [test, sbom]
    if: github.event_name == 'push' && github.ref == 'refs/heads/main'
    permissions:
      id-token: write
      contents: read
    steps:
    - uses: actions/checkout@v4
    
    - name: Install Rust
      uses: dtolnay/rust-toolchain@stable
      with:
        toolchain: ${{{{ env.RUST_VERSION }}}}
        targets: x86_64-unknown-linux-musl
    
    - name: Build release
      run: cargo build --release --target x86_64-unknown-linux-musl
    
    - name: Install Cosign
      uses: sigstore/cosign-installer@v3
    
    - name: Sign binary
      run: |
        cosign sign-blob \
          --yes \
          --output-signature target/x86_64-unknown-linux-musl/release/{}.sig \
          --output-certificate target/x86_64-unknown-linux-musl/release/{}.crt \
          target/x86_64-unknown-linux-musl/release/{}
"#,
        plan.project.rust_version.as_deref().unwrap_or("stable"),
        plan.project.name,
        plan.project.name,
        plan.project.name,
    );

    fs::write(workflow_dir.join("ci.yml"), ci_content)?;
    files.push(".github/workflows/ci.yml".to_string());

    Ok(())
}

