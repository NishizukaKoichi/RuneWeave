pub fn generate_scaffold(plan: &Plan, seed: u64, output_dir: &Path) -> Result<Vec<String>> {
    let mut rng = ChaCha8Rng::seed_from_u64(seed);
    let mut files_generated = Vec::new();

    fs::create_dir_all(output_dir)?;

    let mut tera = Tera::default();
    load_templates(&mut tera)?;

    let mut context = Context::new();
    context.insert("project", &plan.project);
    context.insert("services", &plan.services);
    context.insert("tools", &plan.tools);
    context.insert("infrastructure", &plan.infrastructure);

    write_workspace_cargo_toml(output_dir, &plan, &mut files_generated)?;
    write_rust_toolchain(output_dir, &plan, &mut files_generated)?;
    write_gitignore(output_dir, &mut files_generated)?;

    for service in &plan.services {
        generate_service(output_dir, service, &plan, &mut tera, &mut context, &mut files_generated)?;
    }

    if let Some(tools) = &plan.tools {
        for tool in tools {
            generate_tool(output_dir, tool, &plan, &mut tera, &mut context, &mut files_generated)?;
        }
    }

    write_github_workflows(output_dir, &plan, &mut tera, &mut context, &mut files_generated)?;

    Ok(files_generated)
}

fn write_workspace_cargo_toml(
    output_dir: &Path,
    plan: &Plan,
    files: &mut Vec<String>,
) -> Result<()> {
    let mut members = vec![];

    for service in &plan.services {
        members.push(format!("services/{}", service.name));
    }

    if let Some(tools) = &plan.tools {
        for tool in tools {
            members.push(format!("tools/{}", tool.name));
        }
    }

    let content = format!(
        r#"[workspace]
members = {}
resolver = "2"

[workspace.package]
version = "{}"
edition = "2021"
rust-version = "{}"
authors = ["Generated by RuneWeave"]
repository = "{}"
license = "{}"

[workspace.dependencies]
tokio = {{ version = "1", features = ["full"] }}
serde = {{ version = "1", features = ["derive"] }}
serde_json = "1"
anyhow = "1"
thiserror = "1"
tracing = "0.1"
tracing-subscriber = "0.3"

[profile.release]
opt-level = 3
lto = true
codegen-units = 1
strip = true

[profile.release-wasm]
inherits = "release"
opt-level = "z"
"#,
        serde_json::to_string(&members)?,
        plan.project.version,
        plan.project.rust_version.as_deref().unwrap_or("1.80"),
        plan.project.repository.as_deref().unwrap_or(""),
        plan.project.license.as_deref().unwrap_or("MIT OR Apache-2.0"),
    );

    let path = output_dir.join("Cargo.toml");
    fs::write(&path, content)?;
    files.push("Cargo.toml".to_string());
    
    Ok(())
}

fn write_rust_toolchain(
    output_dir: &Path,
    plan: &Plan,
    files: &mut Vec<String>,
) -> Result<()> {
    let rust_version = plan.project.rust_version.as_deref().unwrap_or("stable");
    
    let content = format!(
        r#"[toolchain]
channel = "{}"
components = ["rustfmt", "clippy"]
targets = ["wasm32-unknown-unknown", "x86_64-unknown-linux-musl"]
profile = "minimal"
"#,
        rust_version
    );

    let path = output_dir.join("rust-toolchain.toml");
    fs::write(&path, content)?;
    files.push("rust-toolchain.toml".to_string());
    
    Ok(())
}

